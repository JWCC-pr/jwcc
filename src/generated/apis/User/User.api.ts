export class UserApi<
  SecurityDataType = unknown,
> extends HttpClient<SecurityDataType> {
  /**
   * No description
   *
   * @tags user
   * @name UserLoginCreate
   * @summary 유저 로그인
   * @request POST:/v1/user/login/
   * @secure
   */

  userLoginCreate = (variables: {
    data: DeepOmitReadOnly<UserLoginRequestType>
    params?: RequestParams
  }) =>
    this.request<UserLoginType, UserLoginErrorMessageType | CommonErrorType>({
      path: `/v1/user/login/`,
      method: 'POST',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * @description 이메일을 통해 비밀번호 재설정 가능한 link을 발급받습니다.
   *
   * @tags user
   * @name UserPasswordResetCreate
   * @summary 유저 비밀번호 초기화 메일 발송
   * @request POST:/v1/user/password_reset/
   * @secure
   */

  userPasswordResetCreate = (variables: {
    data: DeepOmitReadOnly<UserPasswordResetRequestType>
    params?: RequestParams
  }) =>
    this.request<
      UserPasswordResetType,
      UserPasswordResetErrorMessageType | CommonErrorType
    >({
      path: `/v1/user/password_reset/`,
      method: 'POST',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * @description 유저 비밀번호 초기화 메일 발송 API를 통해 발급 받은 link를 통해 비밀번호를 재설정합니다.
   *
   * @tags user
   * @name UserPasswordResetConfirmCreate
   * @summary 유저 비밀번호 재설정
   * @request POST:/v1/user/password_reset_confirm/
   * @secure
   */

  userPasswordResetConfirmCreate = (variables: {
    data: DeepOmitReadOnly<UserPasswordResetConfirmRequestType>
    params?: RequestParams
  }) =>
    this.request<
      UserPasswordResetConfirmType,
      UserPasswordResetConfirmErrorMessageType | CommonErrorType
    >({
      path: `/v1/user/password_reset_confirm/`,
      method: 'POST',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * No description
   *
   * @tags user
   * @name UserRefreshCreate
   * @summary 유저 리프레시
   * @request POST:/v1/user/refresh/
   * @secure
   */

  userRefreshCreate = (variables: {
    data: DeepOmitReadOnly<UserRefreshRequestType>
    params?: RequestParams
  }) =>
    this.request<
      UserRefreshType,
      UserRefreshErrorMessageType | CommonErrorType
    >({
      path: `/v1/user/refresh/`,
      method: 'POST',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * No description
   *
   * @tags user
   * @name UserRegisterCreate
   * @summary 유저 회원가입
   * @request POST:/v1/user/register/
   * @secure
   */

  userRegisterCreate = (variables: {
    data: DeepOmitReadOnly<UserRegisterRequestType>
    params?: RequestParams
  }) =>
    this.request<
      UserRegisterType,
      UserRegisterErrorMessageType | CommonErrorType
    >({
      path: `/v1/user/register/`,
      method: 'POST',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * No description
   *
   * @tags user
   * @name UserRetrieve
   * @summary 유저 조회
   * @request GET:/v1/user/{id}/
   * @secure
   */

  userRetrieve = (variables: { id: string; params?: RequestParams }) =>
    this.request<UserType, CommonErrorType>({
      path: `/v1/user/${variables.id}/`,
      method: 'GET',
      secure: true,
      format: 'json',
      ...variables.params,
    }) /**
   * No description
   *
   * @tags user
   * @name UserUpdate
   * @summary 유저 정보 수정
   * @request PUT:/v1/user/{id}/
   * @secure
   */

  userUpdate = (variables: {
    id: string
    data: DeepOmitReadOnly<UserUpdateRequestType>
    params?: RequestParams
  }) =>
    this.request<UserUpdateType, UserUpdateErrorMessageType | CommonErrorType>({
      path: `/v1/user/${variables.id}/`,
      method: 'PUT',
      body: variables.data,
      secure: true,
      type: ContentType.Json,
      format: 'json',
      ...variables.params,
    }) /**
   * No description
   *
   * @tags user
   * @name UserDestroy
   * @summary 유저 삭제(탈퇴)
   * @request DELETE:/v1/user/{id}/
   * @secure
   */

  userDestroy = (variables: { id: string; params?: RequestParams }) =>
    this.request<void, CommonErrorType>({
      path: `/v1/user/${variables.id}/`,
      method: 'DELETE',
      secure: true,
      ...variables.params,
    })
}


import { InfiniteData, useInfiniteQuery, useQuery } from '@tanstack/react-query'

import { fetchExtended } from '@/configs/fetch/fetch-extend'

import { CommonErrorType } from '../@types/data-contracts'
import {
  InfiniteQueryHookParams,
  Parameter,
  QueryHookParams,
  RequestFnReturn,
} from '../@types/react-query-type'
import { PassingNoticeApi } from './PassingNotice.api'

export const passingNoticeApi = new PassingNoticeApi({
  customFetch: fetchExtended,
})

/**
 * query key 에 undefined 를 포함시키지 않기 위한 함수입니다.
 */
const isDefined = (v: unknown) => typeof v !== 'undefined'

/**
 * query-keys
 */

export const QUERY_KEY_PASSING_NOTICE_API = {
  LIST: (variables?: Parameter<typeof passingNoticeApi.passingNoticeList>) =>
    ['PASSING_NOTICE_LIST', variables].filter(isDefined),
  LIST_INFINITE: (
    variables?: Parameter<typeof passingNoticeApi.passingNoticeList>,
  ) => ['PASSING_NOTICE_LIST_INFINITE', variables].filter(isDefined),
  RETRIEVE: (
    variables?: Parameter<typeof passingNoticeApi.passingNoticeRetrieve>,
  ) => ['PASSING_NOTICE_RETRIEVE', variables].filter(isDefined),
}

/**
 * No description
 *
 * @tags passing_notice
 * @name PassingNoticeList
 * @summary 선종 안내 목록 조회
 * @request GET:/v1/passing_notice/
 * @secure    */

export const usePassingNoticeListQuery = <
  TData = RequestFnReturn<typeof passingNoticeApi.passingNoticeList>,
>(
  params?: QueryHookParams<
    typeof passingNoticeApi.passingNoticeList,
    { error: CommonErrorType },
    TData
  >,
) => {
  const queryKey = QUERY_KEY_PASSING_NOTICE_API.LIST(params?.variables)
  return useQuery({
    queryKey,
    queryFn: () => passingNoticeApi.passingNoticeList(params?.variables),
    ...params?.options,
  })
}

/**
 * No description    *      * @tags passing_notice
 * @name PassingNoticeList
 * @summary 선종 안내 목록 조회
 * @request GET:/v1/passing_notice/
 * @secure    */

export const usePassingNoticeListInfiniteQuery = <
  TData = InfiniteData<
    RequestFnReturn<typeof passingNoticeApi.passingNoticeList>,
    Parameter<typeof passingNoticeApi.passingNoticeList>
  >,
>(
  params?: InfiniteQueryHookParams<
    typeof passingNoticeApi.passingNoticeList,
    CommonErrorType,
    TData
  >,
) => {
  const queryKey = QUERY_KEY_PASSING_NOTICE_API.LIST_INFINITE(params?.variables)
  return useInfiniteQuery({
    queryKey,
    initialPageParam: null,
    queryFn: ({ pageParam }) => {
      const offset = pageParam ?? params?.variables?.query?.offset ?? 0
      return passingNoticeApi.passingNoticeList({
        ...params?.variables,
        query: { ...params?.variables?.query, offset },
      })
    },
    getNextPageParam: (lastPage, allPages) => {
      if (!lastPage?.isNext) return null
      const fetchedLength = allPages?.length || 0
      const initialOffset = params?.variables?.query?.offset || 0
      const limit = params?.variables?.query?.limit || 0

      return initialOffset + fetchedLength * limit
    },
    ...params?.options,
  })
}

/**
 * No description
 *
 * @tags passing_notice
 * @name PassingNoticeRetrieve
 * @summary 선종 안내 상세 조회
 * @request GET:/v1/passing_notice/{id}/
 * @secure    */

export const usePassingNoticeRetrieveQuery = <
  TData = RequestFnReturn<typeof passingNoticeApi.passingNoticeRetrieve>,
>(
  params: QueryHookParams<
    typeof passingNoticeApi.passingNoticeRetrieve,
    { error: CommonErrorType },
    TData
  >,
) => {
  const queryKey = QUERY_KEY_PASSING_NOTICE_API.RETRIEVE(params.variables)
  return useQuery({
    queryKey,
    queryFn: () => passingNoticeApi.passingNoticeRetrieve(params.variables),
    ...params?.options,
  })
}

